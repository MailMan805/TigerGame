/// <summary>
/// Contains data and variables for a specific night level.
/// </summary>
public struct Night
{
    /// <summary>
    /// Inital number of bodies within the level.
    /// </summary>
    public int BodyCount;

    /// <summary>
    /// Contains data and variables for a specific night level.
    /// </summary>
    public Night(int BodyCount)
    {
        this.BodyCount = BodyCount;
    }
}